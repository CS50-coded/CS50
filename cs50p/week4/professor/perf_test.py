def timeitt(runs: int = 100):
    def decorator(f):
        def wrapper(*args, **kwargs):
            stats = []
            for _ in range(runs):
                start = time.perf_counter()
                result = f(*args, **kwargs)
                end = time.perf_counter()
                stats.append(end - start)

            all_args = list((str(arg) for arg in args))
            all_args += list(f"{key} = {value}" for key, value in kwargs.items())
            print(f"{f.__name__}({", ".join(all_args)}) \t| {runs:,} runs | AVG run time: {sum(stats) / runs:.8f}")
            return result

        return wrapper

    return decorator


